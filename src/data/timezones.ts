  export const TIMEZONE_GROUPS = {
    AMERICAS: ALL_TIMEZONES.filter(tz => tz.timezone.startsWith('America')),
    EUROPE: ALL_TIMEZONES.filter(tz => tz.timezone.startsWith('Europe')),
    ASIA: ALL_TIMEZONES.filter(tz => tz.timezone.startsWith('Asia')),
    AFRICA: ALL_TIMEZONES.filter(tz => tz.timezone.startsWith('Africa')),
    OCEANIA: ALL_TIMEZONES.filter(tz => 
      tz.timezone.startsWith('Australia') || 
      tz.timezone.startsWith('Pacific')
    ),
  } as const;
  
  export const getTimezoneByCountryCode = (code: string): TimeZoneData | undefined => {
    return ALL_TIMEZONES.find(tz => tz.shortCode === code.toUpperCase());
  };
  
  export const getTimezoneByCountry = (country: string): TimeZoneData | undefined => {
    return ALL_TIMEZONES.find(tz => 
      tz.country.toLowerCase() === country.toLowerCase()
    );
  };
  